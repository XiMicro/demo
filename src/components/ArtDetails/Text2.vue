<script>
export default {
    name: 'Text2',
    data () {
        return {
            num: -1,
            timetop: 50.5,
            endY: 0,
            direction: 1
        }
    },
    mounted () {
        window.addEventListener('scroll', this.handleScroll)
        window.addEventListener('touchmove', this.touchMove)
    },
    watch:{
        endY:function(val,oldval){
            if(val-oldval<0){
                this.direction=1
            }else if(val-oldval>0){
                this.direction=2
            }
        },
        timetop:function(newval,oldval){
            if(newval-oldval>0){
                this.num++
            }else if(newval-oldval<0){
                this.num--
            }
        }
    },
    methods: {
        touchMove(e){
            if(e.touches.length == 1) {    //等于1时表示此时有只有一只手指在触摸屏幕
                this.endY = e.touches[0].clientY; // 记录开始位置
            }
        },
        handleScroll(){
            if(this.direction==1){
                if(window.pageYOffset>this.timetop){
                    this.timetop=this.timetop+53
                    // console.log(this.num)
                }
            }else if(this.direction==2){
                if(window.pageYOffset+50.5<this.timetop){
                    if(this.timetop>0){
                        this.timetop=this.timetop-53
                        // console.log(this.num)
                    } 
                }
            }
        }
    }
}
</script>
